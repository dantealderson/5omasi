/// Helper function to get translated string.
/// Usage: tr(context, 'key')
String tr(BuildContext context, String key) {
  final locale = Provider.of<LocaleProvider>(context).locale;
  return AppLocalizations.translate(locale, key);
}

/// 12-hour clock with a localized am/pm marker: "10:00 م" / "10:00 PM".
/// Usage: trTime(context, dateTime)
String trTime(BuildContext context, DateTime time) {
  final hour = time.hour % 12 == 0 ? 12 : time.hour % 12;
  final minute = time.minute.toString().padLeft(2, '0');
  final period = time.hour >= 12 ? tr(context, 'pm') : tr(context, 'am');
  return '$hour:$minute $period';
}

